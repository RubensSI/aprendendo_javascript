// soma
let soma = (valor1, valor2) => {
  return `Soma ${valor1} + ${valor2} = ${valor1 + valor2}`
}

// subtração
let subtracao = (valor1, valor2) => {
  return `Subtração ${valor1} - ${valor2} = ${valor1 - valor2}`
}

// divisão
let divisao = (valor1, valor2) => {
  return `Divisão ${valor1} / ${valor2} = ${valor1 / valor2}`
}

// multiplicação
let multiplicacao = (valor1, valor2) => {
  return `Multiplicação ${valor1} * ${valor2} = ${valor1 * valor2}`
}

console.log(soma(2, 3))
console.log(subtracao(4, 3))
console.log(divisao(5, 2))
console.log(multiplicacao(3, 9))



